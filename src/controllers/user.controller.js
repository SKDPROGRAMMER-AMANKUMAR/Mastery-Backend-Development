const getUserChannelProfile = asyncHandler(async (req, res) => {
  const { username } = req.params;

  if (!username?.trim()) {
    throw new ApiError(400, "Username is required");
  }

  const channel = await User.aggregate([

    {
      $match: {
        username: username?.toLowerCase(),
      },
    },
    {
      $lookup: {
        from: "subscriptions",
        localField: "_id",
        foreignField: "channel",
        as: "subscribers",
      },
    },
    {
      $lookup: {
        from: "subscriptions",
        localField: "_id",
        foreignField: "subscriber",
        as: "subscribedTo",
      },
    },
    {
      $addFields: {

        subscriberCount: {
          $size: "$subscribers",
        },
         
        channelsSubscribedToCount: {
          $size: "$subscribedTo",
        },
        isSubscribed: {
          $cond: {
            if: { $in: [req.user?._id, "$subscribers.subscriber"] },
            then: true, 
            else: false,
          },
        },
      },
    },
    {
        $project:{
          fullName:1,  
          username:1, 
          email:1,
          avatar:1,
          coverImage:1,
          subscriberCount:1,
          channelsSubscribedToCount:1, 
          isSubscribed:1,

        }
    }
  ]);

  console.log("Data achived by (channel) aggregation pipeline is:- ",channel);

  if(!channel?.length){
    throw new ApiError(404, "channel does not exist");
  }

  return res
  .status(200)
  .json(new ApiResponse(200, channel[0], "User's channel profile retrieved successfully"));

});

